function show(cl){
    if(cl == 'all'){
        showAll();
    }else{
        for(let i = 0; i < items.length; i++){
            var a = document.querySelectorAll('.'+items[i][0].type);
            if(items[i][0].type != cl){
                for(let j = 0; j < a.length; j++){
                    a[j].style.display = "none";
                }
            } else{
                for(let j = 0; j <a.length; j++){
                    a[j].style.display = "block";
                }
            }
        }
    }
    document.getElementById('orderNow').innerHTML = '<div id="popup"></div> <input id="order-btn" type="button" value="Order Now" name="Order Now" onclick="orderNow()"/>';
    document.getElementById('aboutUs').style.display = 'none';
}